/**
 * Seed peer student corpus for GraphRAG similarity retrieval.
 *
 * Each peer is a synthetic student with a mastery profile across the KNTT
 * curriculum. The graph builder turns these into `peer` nodes, computes a
 * feature vector from their mastery, and the retriever fetches the K most
 * similar peers to the current student for narratives like:
 *   "12 bạn cùng lớp với hồ sơ tương tự đã cải thiện 35% sau khi …"
 *
 * Each peer also references one or more `peerOutcomeId`s — these point to
 * `peerOutcomes.ts` and tell the narrative which activities worked for
 * them. This gives concrete success stories instead of vague advice.
 */

import type { Grade, Goal } from '../types/user'

export interface PeerSeed {
  id: string
  displayName: string
  avatar: string
  grade: Grade
  goal: Goal
  /** ~6-12 topic ids with their (mock) mastery 0..1. Unspecified → 0.5. */
  mastery: Record<string, number>
  /** XP earned. */
  totalXp: number
  currentStreak: number
  /** One-line tag — shown in graph cards. */
  highlight: string
  /** Outcome ids this peer experienced. */
  peerOutcomeIds: string[]
}

